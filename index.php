                       <h1>¿Qué son los paradigmas de programación?</h1>
<h2>¿Qué es un paradigma de programación?</h2>
<p>Un paradigma de programación es una manera o estilo de programación de software. Existen diferentes formas de diseñar un lenguaje de programación y varios modos de trabajar para obtener los resultados que necesitan los programadores.  Se trata de un conjunto de métodos sistemáticos aplicables en todos los niveles del diseño de programas para resolver problemas computacionales.</p> 
<p>Los lenguajes de programación adoptan uno o varios paradigmas en función del tipo de órdenes que permiten implementar como, por ejemplo, Python o JavaScript, que son multiparadigmas.
</p>

<h2>Paradigma imperativo</h2>
<p>Los programas consisten en una sucesión de instrucciones o conjunto de sentencias, como si el programador diera órdenes concretas. El desarrollador describe en el código paso por paso todo lo que hará su programa.

    Algunos lenguajes: Pascal, COBOL, FORTRAN, C, C++, etc.
     </p>
<h2>Otros enfoques subordinados al paradigma de programación imperativa son:</h2>

<p>Programación estructurada: La programación estructurada es un tipo de programación imperativa donde el flujo de control se define mediante bucles anidados, condicionales y subrutinas, en lugar de a través de GOTO.
    Programación procedimental: Este paradigma de programación consiste en basarse en un número muy bajo de expresiones repetidas, englobarlas todas en un procedimiento o función y llamarlo cada vez que tenga que ejecutarse.
    Programación modular: consiste en dividir un programa en módulos o subprogramas con el fin de hacerlo más manejable y legible. Se trata de una evolución de la programación estructurada para resolver problemas de programación más complejos.
    Paradigma declarativo
    Este paradigma no necesita definir algoritmos puesto que describe el problema en lugar de encontrar una solución al mismo. Este paradigma utiliza el principio del razonamiento lógico para responder a las preguntas o cuestiones consultadas.</p>

<h2>Este paradigma a su vez se divide en dos:</h2>
<ul>
    <li>Programación Lógica: Prolog</li>
    <li>Programación funcional: Lisp, Scala, Java, Kotlin</li>
    <li>Programación orientada a objetos</li>
</ul>


